/* Interactive SpeechDx leaderboard — renders window.LEADERBOARD_DATA
   (generated by scripts/build_site_data.py). Rows = models, columns = tasks
   coloured by speech-production category; dark/light theme; an "average similar
   tasks" toggle switches between the raw (27) and merged (19) task views. */
(function () {
  "use strict";

  const DATA = window.LEADERBOARD_DATA;
  if (!DATA) { document.getElementById("board").textContent = "data.js failed to load."; return; }

  // category colour (hex) + "R G B" triplet + label, keyed by code
  const catColor = {}, catRgb = {}, catLabel = {};
  DATA.categories.forEach(c => {
    catColor[c.code] = c.color; catLabel[c.code] = c.label;
    const h = c.color.replace("#", "");
    catRgb[c.code] = [0, 2, 4].map(i => parseInt(h.slice(i, i + 2), 16)).join(" ");
  });

  // AUC strips the leading 0 (".989"); MAE keeps 2 decimals
  const fmt = (v, type) =>
    (v == null ? "—" : type === "regression" ? v.toFixed(2) : v.toFixed(3).replace(/^0\./, "."));

  // ---- state ----
  let merged = true;                   // "average similar tasks" — default ON (shows all 24 incl. the 7B rows)
  let sortKey = "mrr", sortDir = -1;    // -1 desc, +1 asc
  const V = () => (merged ? DATA.views.merged : DATA.views.raw);

  let curTasks = [], bestByTask = {}, maxMrr = 0;

  // ---- legend (built once) ----
  const legend = document.getElementById("legend");
  legend.innerHTML =
    `<div class="legend-group"><span class="legend-title">Category</span>` +
      DATA.categories.map(c =>
        `<span class="legend-item"><span class="swatch-cat" style="background:rgb(${catRgb[c.code]} / .85)"></span>${c.label}</span>`
      ).join("") +
    `</div>` +
    `<div class="legend-group"><span class="legend-title">Metric</span>` +
      `<span class="legend-item">AUC — higher better</span>` +
      `<span class="legend-item">MAE ↓ — lower better</span>` +
    `</div>`;

  // ---- info box (locked bottom-centre; tap/hover to show, × or tap-away to dismiss) ----
  const tip = document.getElementById("tooltip");
  function showTip(html) {
    tip.innerHTML = html + '<button class="tt-close" aria-label="Dismiss">&times;</button>';
    tip.hidden = false;
  }
  function hideTip() { tip.hidden = true; }
  tip.addEventListener("click", e => { e.stopPropagation(); if (e.target.closest(".tt-close")) hideTip(); });
  document.addEventListener("click", () => { if (!tip.hidden) hideTip(); });
  document.querySelector(".table-wrap").addEventListener("mouseleave", hideTip);

  // ---- sorting ----
  function sortedModels() {
    const ms = V().models.slice();
    ms.sort((a, b) => {
      if (sortKey === "model") {
        const x = a.short.toLowerCase(), y = b.short.toLowerCase();
        return x < y ? sortDir : x > y ? -sortDir : 0;
      }
      let av, bv;
      if (sortKey === "mrr") { av = a.mrr; bv = b.mrr; }
      else { av = a.scores[sortKey]; bv = b.scores[sortKey]; }
      if (av == null && bv == null) return 0;
      if (av == null) return 1;
      if (bv == null) return -1;
      return (av - bv) * sortDir;
    });
    return ms;
  }
  function setSort(key) {
    if (sortKey === key) sortDir *= -1;
    else { sortKey = key; sortDir = key === "model" ? 1 : -1; }
    renderBody(); renderSortIndicators();
  }
  const arrow = () => (sortDir === -1 ? "▼" : "▲");

  // ---- table ----
  const table = document.getElementById("board");
  let hrow, tbody;

  function renderMeta() {
    document.getElementById("meta").innerHTML =
      `${DATA.n_models} models · ${V().n_tasks} tasks · generated ${DATA.generated} · ` +
      `<a href="${DATA.repo_url}" target="_blank" rel="noopener">GitHub repo ↗</a> · ` +
      `<a href="${DATA.lab_url}" target="_blank" rel="noopener">CHAI Lab ↗</a> · ` +
      `by <a href="${DATA.author_url}" target="_blank" rel="noopener">${DATA.author}</a>`;
  }

  function buildHeader() {
    const thead = document.createElement("thead");
    hrow = document.createElement("tr");

    const thModel = document.createElement("th");
    thModel.className = "col-model th-sortable"; thModel.dataset.key = "model";
    thModel.innerHTML = `<div class="th-inner">Model</div>`;
    hrow.appendChild(thModel);

    const thMrr = document.createElement("th");
    thMrr.className = "col-mrr th-sortable"; thMrr.dataset.key = "mrr";
    thMrr.innerHTML = `<div class="th-inner">MRR</div>`;
    hrow.appendChild(thMrr);

    curTasks.forEach(t => {
      const th = document.createElement("th");
      th.className = "th-task th-sortable"; th.dataset.key = t.id;
      th.style.setProperty("--cat", catColor[t.category]);
      const tag = t.type === "regression" ? "MAE&nbsp;↓" : "AUC";
      th.innerHTML =
        `<div class="th-inner"><span class="task-tnum">${t.tnum}</span>` +
        `<span class="task-label">${t.slabel}</span>` +
        `<span class="task-metric">${tag}</span></div>`;
      const tipHtml =
        `<div class="tt-title">${t.tnum} · ${t.label}</div><div>${t.desc}</div>` +
        `<div class="tt-sub">${t.metric} · ${catLabel[t.category]}</div>`;
      th.addEventListener("mouseenter", () => showTip(tipHtml));
      th.addEventListener("click", e => { showTip(tipHtml); e.stopPropagation(); });
      hrow.appendChild(th);
    });

    thead.appendChild(hrow);
    table.appendChild(thead);
    hrow.querySelectorAll(".th-sortable").forEach(th =>
      th.addEventListener("click", () => setSort(th.dataset.key)));
  }

  function renderSortIndicators() {
    hrow.querySelectorAll(".th-sortable").forEach(th => {
      th.querySelector(".sort-arrow")?.remove();
      th.classList.toggle("sort-active", th.dataset.key === sortKey);
      if (th.dataset.key === sortKey) {
        const s = document.createElement("span");
        s.className = "sort-arrow"; s.textContent = arrow();
        th.querySelector(".th-inner").appendChild(s);
      }
    });
  }

  function renderBody() {
    if (tbody) tbody.remove();
    tbody = document.createElement("tbody");
    table.appendChild(tbody);
    sortedModels().forEach(m => {
      const tr = document.createElement("tr");

      const tdM = document.createElement("td");
      tdM.className = "col-model cell-model";
      tdM.innerHTML = `<span class="rank">${m.rank}</span><span class="name">${m.short}</span>`;
      const mHtml = `<div class="tt-title">${m.display}</div>` +
        `<div class="tt-sub">checkpoint · ${m.host}</div>` +
        `<a class="tt-link" href="${m.repo_url}" target="_blank" rel="noopener">${m.repo} ↗</a>` +
        `<div class="tt-rev">revision <code>${m.revision}</code> · ${m.revision_date}</div>`;
      tdM.addEventListener("mouseenter", () => showTip(mHtml));
      tdM.addEventListener("click", e => { showTip(mHtml); e.stopPropagation(); });
      tr.appendChild(tdM);

      const tdR = document.createElement("td");
      tdR.className = "col-mrr cell-mrr";
      const w = maxMrr ? Math.round((m.mrr / maxMrr) * 100) : 0;
      tdR.innerHTML = `<div class="mrr-val">${m.mrr.toFixed(3).replace(/^0\./, ".")}</div>` +
        `<div class="mrr-track"><div class="mrr-bar" style="width:${w}%"></div></div>`;
      tr.appendChild(tdR);

      curTasks.forEach(t => {
        const v = m.scores[t.id];
        const td = document.createElement("td");
        td.className = "cell-score" + (v == null ? " na" : "");
        td.style.setProperty("--cat-rgb", catRgb[t.category]);
        if (v != null && v === bestByTask[t.id]) td.classList.add("top1");
        td.textContent = fmt(v, t.type);
        tr.appendChild(td);
      });

      tbody.appendChild(tr);
    });
  }

  function render() {
    curTasks = V().tasks;
    bestByTask = {};
    curTasks.forEach(t => {
      const vals = V().models.map(m => m.scores[t.id]).filter(v => v != null);
      bestByTask[t.id] = !vals.length ? null
        : (t.type === "regression" ? Math.min.apply(null, vals) : Math.max.apply(null, vals));
    });
    maxMrr = Math.max.apply(null, V().models.map(m => m.mrr || 0));
    renderMeta();
    table.innerHTML = ""; tbody = null;
    buildHeader();
    renderBody();
    renderSortIndicators();
  }

  // ---- controls: average-similar-tasks checkbox + theme toggle ----
  const mergeCb = document.getElementById("merge-toggle");
  if (mergeCb) mergeCb.addEventListener("change", () => { merged = mergeCb.checked; render(); });

  const themeBtn = document.getElementById("theme-toggle");
  function themeLabel() {
    if (themeBtn) themeBtn.textContent = document.documentElement.dataset.theme === "dark" ? "☀" : "☾";
  }
  if (themeBtn) {
    themeBtn.addEventListener("click", () => {
      const dark = document.documentElement.dataset.theme === "dark";
      document.documentElement.dataset.theme = dark ? "light" : "dark";
      try { localStorage.setItem("sdx-theme", document.documentElement.dataset.theme); } catch (e) {}
      themeLabel();
    });
    themeLabel();
  }

  render();
})();
