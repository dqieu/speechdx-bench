/* Interactive SpeechDx leaderboard — renders window.LEADERBOARD_DATA
   (generated by scripts/build_site_data.py) into a sortable heatmap table. */
(function () {
  "use strict";

  const DATA = window.LEADERBOARD_DATA;
  if (!DATA) { document.getElementById("board").textContent = "data.js failed to load."; return; }

  const CLS = "59,130,246";   // classification / ROC-AUC base (blue)
  const REG = "245,158,11";   // regression / C-index base (amber)

  const catColor = {}, catLabel = {};
  DATA.categories.forEach(c => { catColor[c.code] = c.color; catLabel[c.code] = c.label; });
  const taskById = {};
  DATA.tasks.forEach(t => { taskById[t.id] = t; });
  const maxMrr = Math.max.apply(null, DATA.models.map(m => m.mrr || 0));
  // best model per task for the top-1 outline (regression = MAE → lowest wins)
  const bestByTask = {};
  DATA.tasks.forEach(t => {
    const vals = DATA.models.map(m => m.scores[t.id]).filter(v => v != null);
    bestByTask[t.id] = !vals.length ? null
      : (t.type === "regression" ? Math.min.apply(null, vals) : Math.max.apply(null, vals));
  });

  // ---- colour helpers ------------------------------------------------------
  const lerp = (a, b, t) => a + (b - a) * t;
  function tint(base, alpha) {
    const p = base.split(",").map(Number);
    const r = Math.round(lerp(255, p[0], alpha));
    const g = Math.round(lerp(255, p[1], alpha));
    const b = Math.round(lerp(255, p[2], alpha));
    return `rgb(${r},${g},${b})`;
  }
  // Flat per-column background to differentiate the two metrics (no heatmap):
  // ROC-AUC columns get one tint, MAE columns a slightly different tint.
  const COL_BG = { classification: tint(CLS, 0.14), regression: tint(REG, 0.16) };
  const scoreBg = type => COL_BG[type] || "#ffffff";
  const fmt = (v, type) =>
    (v == null ? "—" : type === "regression" ? v.toFixed(2) : v.toFixed(3));

  // ---- meta + legend -------------------------------------------------------
  const meta = document.getElementById("meta");
  meta.innerHTML =
    `${DATA.n_models} external encoders · ${DATA.n_tasks} tasks · ` +
    `generated ${DATA.generated} · ` +
    `<a href="${DATA.leaderboard_csv_url}">leaderboard.csv on main ↗</a>`;
  document.getElementById("foot-src").innerHTML =
    `Source: <code>${DATA.source}</code> · MRR / metrics mirror ` +
    `<a href="${DATA.leaderboard_csv_url}">leaderboard.csv</a> on the <code>main</code> branch.`;

  const legend = document.getElementById("legend");
  const catItems = DATA.categories.map(c =>
    `<span class="legend-item"><span class="swatch-cat" style="border-color:${c.color}"></span>${c.label}</span>`
  ).join("");
  legend.innerHTML =
    `<div class="legend-group"><span class="legend-title">Category</span>${catItems}</div>` +
    `<div class="legend-group"><span class="legend-title">Metric</span>` +
      `<span class="legend-item"><span class="swatch" style="background:${COL_BG.classification}"></span>Classification · ROC-AUC (higher better)</span>` +
      `<span class="legend-item"><span class="swatch" style="background:${COL_BG.regression}"></span>Regression · MAE (lower better)</span>` +
    `</div>`;

  // ---- tooltip -------------------------------------------------------------
  const tip = document.getElementById("tooltip");
  function showTip(html, e) {
    tip.innerHTML = html; tip.hidden = false; moveTip(e);
  }
  function moveTip(e) {
    const pad = 14, w = tip.offsetWidth, h = tip.offsetHeight;
    let x = e.clientX + pad, y = e.clientY + pad;
    if (x + w > window.innerWidth - 8) x = e.clientX - w - pad;
    if (y + h > window.innerHeight - 8) y = e.clientY - h - pad;
    tip.style.left = x + "px"; tip.style.top = y + "px";
  }
  function hideTip() { tip.hidden = true; }

  // ---- sorting state -------------------------------------------------------
  let sortKey = "mrr", sortDir = -1;   // -1 desc, 1 asc
  function sortedModels() {
    const ms = DATA.models.slice();
    ms.sort((a, b) => {
      let av, bv;
      if (sortKey === "model") { av = a.display.toLowerCase(); bv = b.display.toLowerCase(); return av < bv ? sortDir : av > bv ? -sortDir : 0; }
      if (sortKey === "mrr") { av = a.mrr; bv = b.mrr; }
      else { av = a.scores[sortKey]; bv = b.scores[sortKey]; }
      if (av == null && bv == null) return 0;
      if (av == null) return 1;            // nulls always last
      if (bv == null) return -1;
      return (av - bv) * sortDir;
    });
    return ms;
  }
  function setSort(key) {
    if (sortKey === key) { sortDir *= -1; }
    else { sortKey = key; sortDir = key === "model" ? 1 : -1; }
    renderBody();
    renderSortIndicators();
  }
  const arrow = () => (sortDir === -1 ? "▼" : "▲");

  // ---- header --------------------------------------------------------------
  const table = document.getElementById("board");
  const thead = document.createElement("thead");
  const hrow = document.createElement("tr");

  const thModel = document.createElement("th");
  thModel.className = "col-model th-sortable";
  thModel.dataset.key = "model";
  thModel.innerHTML = `<div class="th-inner">Model</div>`;
  hrow.appendChild(thModel);

  const thMrr = document.createElement("th");
  thMrr.className = "col-mrr th-sortable";
  thMrr.dataset.key = "mrr";
  thMrr.innerHTML = `<div class="th-inner">MRR</div>`;
  hrow.appendChild(thMrr);

  DATA.tasks.forEach(t => {
    const th = document.createElement("th");
    th.className = "th-task th-sortable";
    th.dataset.key = t.id;
    th.style.setProperty("--cat", catColor[t.category]);
    th.innerHTML = `<div class="th-inner"><span class="task-tnum">${t.tnum}</span>` +
                   `<span class="task-label">${t.slabel}</span></div>`;
    th.addEventListener("mouseenter", e => showTip(
      `<div class="tt-title">${t.tnum} · ${t.label}</div><div>${t.desc}</div>` +
      `<div class="tt-sub">${t.metric} · ${catLabel[t.category]} · <code>${t.id}</code></div>`, e));
    th.addEventListener("mousemove", moveTip);
    th.addEventListener("mouseleave", hideTip);
    hrow.appendChild(th);
  });

  thead.appendChild(hrow);
  table.appendChild(thead);
  const tbody = document.createElement("tbody");
  table.appendChild(tbody);

  // place the sticky MRR column right after the model column
  function placeMrrColumn() {
    const left = thModel.offsetWidth;
    document.querySelectorAll(".col-mrr").forEach(el => { el.style.left = left + "px"; });
  }

  // delegate sort clicks
  hrow.querySelectorAll(".th-sortable").forEach(th =>
    th.addEventListener("click", () => setSort(th.dataset.key)));

  function renderSortIndicators() {
    hrow.querySelectorAll(".th-sortable").forEach(th => {
      const inner = th.querySelector(".th-inner");
      inner.querySelector(".sort-arrow")?.remove();
      th.classList.toggle("sort-active", th.dataset.key === sortKey);
      if (th.dataset.key === sortKey) {
        const s = document.createElement("span");
        s.className = "sort-arrow"; s.textContent = arrow();
        inner.appendChild(s);
      }
    });
  }

  // ---- body ----------------------------------------------------------------
  function renderBody() {
    tbody.innerHTML = "";
    sortedModels().forEach(m => {
      const tr = document.createElement("tr");

      const tdM = document.createElement("td");
      tdM.className = "col-model cell-model";
      tdM.innerHTML =
        `<span class="rank">${m.rank}.</span>` +
        `<span class="name">${m.short}</span>`;
      tdM.addEventListener("mouseenter", e => showTip(
        `<div class="tt-title">${m.display}</div>` +
        `<div class="tt-sub">checkpoint</div><code>${m.checkpoint}</code>`, e));
      tdM.addEventListener("mousemove", moveTip);
      tdM.addEventListener("mouseleave", hideTip);
      tr.appendChild(tdM);

      const tdR = document.createElement("td");
      tdR.className = "col-mrr cell-mrr";
      const w = maxMrr ? Math.round((m.mrr / maxMrr) * 100) : 0;
      tdR.innerHTML =
        `<div class="mrr-val">${m.mrr.toFixed(3)}</div>` +
        `<div class="mrr-track"><div class="mrr-bar" style="width:${w}%"></div></div>`;
      tr.appendChild(tdR);

      DATA.tasks.forEach(t => {
        const v = m.scores[t.id];
        const td = document.createElement("td");
        td.className = "cell-score" + (v == null ? " na" : "");
        td.style.background = scoreBg(t.type);
        if (v != null && v === bestByTask[t.id]) td.classList.add("top1");
        td.textContent = fmt(v, t.type);
        tr.appendChild(td);
      });

      tbody.appendChild(tr);
    });
    placeMrrColumn();
  }

  renderBody();
  renderSortIndicators();
  window.addEventListener("resize", placeMrrColumn);
})();
